"""
MX 프라이싱 엔진 — 정답지 (Lv.2 프라이싱 + Lv.3 손익)
=====================================================
원본 '2-2. 품목별시뮬 (금융리스)_MX' 의 대표 행(r7, 갤럭시탭 SM-X820 4년)을
RRP 입력값과 비용률 파라미터만으로 100% 재현한 검증용 코드.

실행:  python3 pricing_engine.py
"""
import math

# ── 엑셀 PV 함수 재현 (월납·기말) ───────────────────────
def PV(rate, nper, pmt):
    # 엑셀 PV(rate,nper,pmt) = -pmt*(1-(1+rate)^-nper)/rate
    return -pmt * (1 - (1 + rate) ** (-nper)) / rate

# ── 비용률 파라미터 ('1. 품목별시뮬 주요비용률' 시트) ────
RATE = dict(
    거래선수수료=0.126,   # D5 (제품가 比, 삼판)  ※닷컴이면 0
    인프라=0.005,          # D7
    보험료율=0.00553,      # D9 (총구독료 比)
    결합할인=0.0,          # D10
    구독포인트=0.0966,     # D15 (제품가 比)
    중도해지Risk=0.041,    # D17
    선납할인=0.04,         # D20 (총구독료 比)
    PV할인이자율=0.0357,   # D22 (매출인식 구독료 PV용)
    이자비용율=0.04,       # D23 (제품가, 금융리스 이자)
    다품목포인트=0.01497,  # D24
    포인트매출차감=0.07,   # F15
    이자손익율=0.029,      # F23 (실제 조달금리)
)

def pricing(
    RRP=1399200, 혜택가_vplus=1344000,    # 'RRP,CGS' 시트 VLOOKUP 값
    보증판가=96000, 보증원가=9600,         # '케어보증 Master'
    케어판가=0, 케어원가=0, 파손보험=115200,
    CGS=690074.16,                          # 'RRP,CGS' 연결원가
    인센=30000, 시상=10000,                # 절대값 입력
    가격조정=2000,                          # CA (수동 100단위 조정)
    개월=48, 년=4, 제품OH율=0.2,
):
    r = RATE
    # ── 1) 혜택가 → 비용 요소들 ────────────────────────
    혜택가_vminus = round(혜택가_vplus / 1.1, 0)            # AL
    이자비용 = 혜택가_vminus - PV(r['이자비용율']/12, 개월, -혜택가_vminus/개월)  # AU
    거래선수수료 = 혜택가_vminus * r['거래선수수료']        # AW
    인프라 = 혜택가_vminus * r['인프라']                    # BA
    중도해지Risk = 혜택가_vminus * r['중도해지Risk']        # BB
    결합할인 = 혜택가_vminus * r['결합할인']                # BC
    구독포인트 = 혜택가_vminus * r['구독포인트']            # BD
    다품목포인트 = 혜택가_vminus * r['다품목포인트']        # BG
    보험료 = (혜택가_vminus + 케어판가 + 보증판가 + 이자비용 + 거래선수수료
              + 인센 + 시상 + 인프라 + 중도해지Risk + 결합할인 + 구독포인트) \
             * r['보험료율'] * 년                            # BE

    # ── 2) 구독료 총합(v-) → 월구독료 → 정수처리 ───────
    구독료총합_vminus = (혜택가_vminus + 케어판가 + 보증판가 + 인센 + 시상
                        + 인프라 + 중도해지Risk + 결합할인 + 구독포인트
                        + 이자비용 + 거래선수수료 + 보험료)          # BI
    순수월할 = round((구독료총합_vminus - 케어판가 - 보증판가) / 개월, -2)  # BO
    보증월할 = 보증판가 / 개월                                # BQ
    케어월할 = round(케어판가 / 개월, -2)                     # BP
    월구독료_vminus = 순수월할 + 케어월할 + 보증월할          # BS
    총구독료_vminus = 월구독료_vminus * 개월                  # BR

    # ── 3) 가격조정 + 파손보험 추가 ───────────────────
    조정월_vminus = 월구독료_vminus + 가격조정                # CB
    조정총_vplus = (조정월_vminus * 1.1) * 개월               # CE
    조정총_vminus = 조정총_vplus / 1.1                        # CF
    파손포함총_vminus = 조정총_vminus + 파손보험              # CI  ★MX
    매출인식구독료_vminus = 조정총_vminus - (케어월할+보증월할)*개월  # CX

    # ── 4) 손익(P&L) ─────────────────────────────────
    월구독료_매출인식 = 매출인식구독료_vminus / 개월          # CY
    순수구독료PV = PV(r['PV할인이자율']/12, 개월, -월구독료_매출인식)  # CZ
    매출인식액 = min(순수구독료PV, 혜택가_vminus)             # DB (제품매출 즉시인식)
    이자수익 = 매출인식구독료_vminus - 매출인식액             # DC
    포인트차감 = -다품목포인트*0 - 매출인식구독료_vminus*r['포인트매출차감']  # DD (AI패키지=0)
    해지충당 = -0.1072 * 혜택가_vminus  # DE 근사(D125 해지율테이블 의존; 참고용)
    원가율 = CGS / 혜택가_vminus                              # DK
    OH = (인센+시상+인프라) + 제품OH율*(혜택가_vminus+포인트차감+해지충당) \
         + 조정총_vminus*r['보험료율']*년 + 파손포함총_vminus*0.08  # DN 근사(F5=0.08)
    제품이익_설치월 = 매출인식액 + 포인트차감 - CGS - OH + 해지충당  # DP
    이자손익 = 이자수익 - (혜택가_vminus - PV(r['이자손익율']/12, 개월, -혜택가_vminus/개월))  # DX
    보증손익 = 보증판가 - 보증원가                            # DW (매출은 BQ*Z=보증판가)
    BEP = math.ceil((CGS + OH - 포인트차감) * 개월 / 매출인식구독료_vminus)  # EB

    return locals()


if __name__ == "__main__":
    p = pricing()
    print("=" * 64)
    print("MX 프라이싱 엔진 정답지 (갤럭시탭 SM-X820, 4년/48개월)")
